import { expect, test } from '@jest/globals';
import genDiff from '../src/gendiff.js';
import stylish from '../src/formatters/stylish.js';
import plain from '../src/formatters/plain';

const data1 = {
  common: {
    setting1: 'Value 1',
    setting2: 200,
    setting3: true,
    setting6: {
      key: 'value',
      doge: {
        wow: '',
      },
    },
  },
  group1: {
    baz: 'bas',
    foo: 'bar',
    nest: {
      key: 'value',
    },
  },
  group2: {
    abc: 12345,
    deep: {
      id: 45,
    },
  },
};

const data2 = {
  common: {
    follow: false,
    setting1: 'Value 1',
    setting3: null,
    setting4: 'blah blah',
    setting5: {
      key5: 'value5',
    },
    setting6: {
      key: 'value',
      ops: 'vops',
      doge: {
        wow: 'so much',
      },
    },
  },
  group1: {
    foo: 'bar',
    baz: 'bars',
    nest: 'str',
  },
  group3: {
    deep: {
      id: {
        number: 45,
      },
    },
    fee: 100500,
  },
};

test('nested data test (stylish format)', () => {
  const expectedResult = `{
    common: {
      + follow: false
        setting1: Value 1
      - setting2: 200
      - setting3: true
      + setting3: null
      + setting4: blah blah
      + setting5: {
            key5: value5
        }
        setting6: {
            doge: {
              - wow: 
              + wow: so much
            }
            key: value
          + ops: vops
        }
    }
    group1: {
      - baz: bas
      + baz: bars
        foo: bar
      - nest: {
            key: value
        }
      + nest: str
    }
  - group2: {
        abc: 12345
        deep: {
            id: 45
        }
    }
  + group3: {
        deep: {
            id: {
                number: 45
            }
        }
        fee: 100500
    }
}`;

  const diffTree = genDiff(data1, data2);

  expect(stylish(diffTree)).toEqual(expectedResult);
});

test('empty data test  (stylish format)', () => {
  const emptyData1 = {};
  const emptyData2 = {};
  const expectedResult = `{

}`;

  const diffTree = genDiff(emptyData1, emptyData2);

  expect(stylish(diffTree)).toEqual(expectedResult);
});

test('nested data test (plain format)', () => {
  const expectedResult = `Property 'common.follow' was added with value: false
Property 'common.setting2' was removed
Property 'common.setting3' was updated. From true to null
Property 'common.setting4' was added with value: 'blah blah'
Property 'common.setting5' was added with value: [complex value]
Property 'common.setting6.doge.wow' was updated. From '' to 'so much'
Property 'common.setting6.ops' was added with value: 'vops'
Property 'group1.baz' was updated. From 'bas' to 'bars'
Property 'group1.nest' was updated. From [complex value] to 'str'
Property 'group2' was removed
Property 'group3' was added with value: [complex value]`;

  const diffTree = genDiff(data1, data2);

  expect(plain(diffTree)).toEqual(expectedResult);
});

test('empty data test (plain format)', () => {
  const emptyData1 = {};
  const emptyData2 = {};
  const expectedResult = '';

  const diffTree = genDiff(emptyData1, emptyData2);

  expect(plain(diffTree)).toEqual(expectedResult);
});
